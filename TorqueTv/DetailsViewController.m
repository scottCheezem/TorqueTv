- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    //static NSString *CellIdentifier = @"Cell";
    //UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    UITableViewCell *cell = [[UITableViewCell alloc]init];
    // Configure the cell...
    if(indexPath.row == 0){
        //thumb cell

        NSURL *imageUrl = [NSURL URLWithString:self.thumbUrl];
        
        //add to queue?
        NSData *imageData = [NSData dataWithContentsOfURL:imageUrl];

        
        
        UIImage *thumb = [UIImage imageWithData:imageData];
        UIImageView *imageView = [[UIImageView alloc]initWithImage:thumb];

        UITableViewCell *cell = [[UITableViewCell alloc]initWithFrame:CGRectMake(0, 0, imageView.frame.size.width, imageView.frame.size.height)];
        [cell addSubview:imageView];

    }
    if(indexPath.row == 1){

        UITableViewCell *cell = [[UITableViewCell alloc]initWithFrame:CGRectMake(0, 0, self.tableView.frame.size.width, self.tableView.frame.size.height)];
        cell.textLabel.text = self.plot;
        
    }
    
    return cell;
}

